# Импортируем библиотеку для работы с математическими функциями
import math

# Запрашиваем у пользователя данные для решения уравнения Шредингера
a = float(input("Введите значение переменной a: "))
b = float(input("Введите значение переменной b: "))
c = float(input("Введите значение переменной c: "))

# Вычисляем дискриминант
d = b**2 - 4*a*c

# Выводим результат
if d > 0:
    x1 = (-b + math.sqrt(d)) / (2 * a)
    x2 = (-b - math.sqrt(d)) / (2 * a)
    print("Дискриминант больше нуля, корни уравнения: x1 = {}, x2 = {}".format(x1, x2))
elif d == 0:
    x = -b / (2 * a)
    print("Дискриминант равен нулю, корень уравнения: x = {}".format(x))
else:
    print("Дискриминант меньше нуля, уравнение не имеет действительных корней")